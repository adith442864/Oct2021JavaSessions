package JavaSessions;

public class DataTypes {

	public static void main(String[] args) {
		
	// data types:
	// primitive data types:
		// Integer family: byte, short, long, int
		// floating family: double, float
		// characters : char
		// boolean : boolean - true/false
		
	// 1. byte
	// size: 8bits = 1 byte
	// range: -128 to 127
		
		byte b =10; //memory allocation
		b=20;
		byte b1=0;
		byte b2=-10;
		System.out.println(b);
		System.out.println(b1);
		
	//2. short
	// size: 16bits = 2 bytes
	// range: -32768 to 32767
		short s = 10;
		
	//3. int
	//size: 4 bytes = 32 bits
	//range : -2^31 to 2^32
		int i=10;
		int i1=-10;
		int p=1000;
		
	//4. long
	//size:8 bytes = 64 bits
	//range: -2^63 to 2^63-1
		long ph=99999;
		long distance=1212121;
		long e=10;
		
	//5. float
	//size: 4 bytes = 32 bits
	//range: after point it can take up to 7 digits:
		float f = 12.33f;
		float g=(float)34.44;
		
	//6. double
	//size: 8 bytes : 64 bits
	//range:after point it can take up to 15 digits:
		double d =12.3333;
		double d1=-12.4444;
		double d3=100; //100.00
		
	//7. Char : only single digit value:
	//size: 16 bits = 2 bytes
		char c = 'a';
		System.out.println(c);
		char c1='1';
		System.out.println(c1);
		char c2='$';
		
		char gender ='m';
		
		//8. boolean: true/false
		//size: ~1 bit
		
		boolean flag =true;
		boolean flag1=false;
		
		
		
		
		
		
		

	}

}
